/*=============== CHANGE BACKGROUND HEADER ===============*/
function scrollHeader() {
    const nav = document.getElementById('header');
    // When the scroll is greater than 200 viewport height, add the scroll-header class to the header tag
    if (this.scrollY >= 80) nav.classList.add('scroll-header');
    else nav.classList.remove('scroll-header');
}
window.addEventListener('scroll', scrollHeader);

/*=============== SHOW SCROLL UP ===============*/
function scrollUp() {
    const scrollUp = document.getElementById('scroll-up');
    // When the scroll is higher than 560 viewport height, add the show-scroll class to the a tag with the scroll-top class
    if (this.scrollY >= 560) scrollUp.classList.add('show-scroll');
    else scrollUp.classList.remove('show-scroll');
}
window.addEventListener('scroll', scrollUp);

/*=============== SCROLL SECTIONS ACTIVE LINK ===============*/
const sections = document.querySelectorAll('section[id]');

function scrollActive() {
    const scrollY = window.pageYOffset;

    sections.forEach(current => {
        const sectionHeight = current.offsetHeight;
        const sectionTop = current.offsetTop - 50;
        const sectionId = current.getAttribute('id');

        if (scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
            document.querySelector('.nav__menu a[href*=' + sectionId + ']').classList.add('active-link');
        } else {
            document.querySelector('.nav__menu a[href*=' + sectionId + ']').classList.remove('active-link');
        }
    });
}
window.addEventListener('scroll', scrollActive);

/*=============== DARK LIGHT THEME ===============*/
const themeButton = document.getElementById('theme-toggle');
const darkTheme = 'dark';
const iconTheme = 'fa-sun'; // This will be used to check for 'fa-sun' for light mode and 'fa-moon' for dark mode

// Previously selected theme (if user selected)
const selectedTheme = localStorage.getItem('selected-theme');
const selectedIcon = localStorage.getItem('selected-icon');

// Get the current theme that the interface has by validating the dark-theme class
const getCurrentTheme = () => document.documentElement.getAttribute('data-theme') === darkTheme ? 'dark' : 'light';

// Set initial theme based on localStorage if available
if (selectedTheme) {
    document.documentElement.setAttribute('data-theme', selectedTheme);
    // Set icon accordingly
    if (selectedIcon === 'fa-moon') {
        themeButton.querySelector('i').classList.add('fa-moon');
        themeButton.querySelector('i').classList.remove('fa-sun');
    } else {
        themeButton.querySelector('i').classList.add('fa-sun');
        themeButton.querySelector('i').classList.remove('fa-moon');
    }
}

// Toggle theme and icon on button click
themeButton.addEventListener('click', () => {
    const currentTheme = getCurrentTheme();
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';

    // Apply the new theme
    document.documentElement.setAttribute('data-theme', newTheme);

    // Toggle the icon
    const icon = themeButton.querySelector('i');
    if (newTheme === 'dark') {
        icon.classList.add('fa-moon');
        icon.classList.remove('fa-sun');
    } else {
        icon.classList.add('fa-sun');
        icon.classList.remove('fa-moon');
    }

    // Save the new theme and icon to localStorage
    localStorage.setItem('selected-theme', newTheme);
    localStorage.setItem('selected-icon', newTheme === 'dark' ? 'fa-moon' : 'fa-sun');
});


